#include <stdio.h>
int main()
{
    printf("int: %ld \n", sizeof(int));
    printf("float: %ld \n", sizeof(float));
    printf("double: %ld \n", sizeof(double));
    printf("char: %ld", sizeof(char));
    return 0;

// The program output displays the corresponding size in bytes for each data type.

// format specifier (%ld)
}